package Day2;

public class Loop_While {

	public static void main(String[] args) {
		// Looping statement
		
		//print 1 to 10 value using while loop.
		
		//increment 
		int i=1;
		
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		//Decrement
		int f=10;
		while (f>=1) {
			System.out.println(f);
			f--;
		}

	}
}
