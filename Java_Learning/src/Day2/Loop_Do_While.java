package Day2;

public class Loop_Do_While {

	public static void main(String[] args) {
		// DO While loop, Print Number 1 to 10
		
		System.out.print("Increment using do while loop");
		int i=1;
		do {
			System.out.println(i);
			i++;
		}while(i<=10);
		
		
		System.out.println("Decrement using do while loop");
		int f=10;
		do {
			System.out.println("f");
			f--;
		}while(f>=0);
	}
}
