package Day2;

public class Variable {

	public static void main(String[] args) {
		
		// How to declare variable
		int a=1;
		byte b=20;
		float c=120.10f;  // in the last F or f required.
		long d=12313511l; // in the last L or l required.
		
		String name="PRINSKUMAR PATEL"; //Double quotation mandatory
		char grade='B';  // single quotation
		
		System.out.println("Int for Number : "+a);
		System.out.println("Byte for small number from -128 to 127 : "+b);
		System.out.println("Point Value : "+c);
		System.out.println("Long Number : "+d);
		System.out.println("String Value : "+name);
		System.out.println("Single Word : "+grade);
		
		

	}

}
