package Day2;

public class Loop_for {

	public static void main(String[] args) {
		
		/*For loop Syntax
		 * for(initialization; condition; increment/decrement)
		 * {
		 * 	Statement 
		 * }
		 * For loop example print 1 to 1
		*/
		
		for(int i=10;i<=10;i++) {
			System.out.println(i);
		}

	}

}
